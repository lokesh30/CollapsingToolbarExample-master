package co.mobiwise.collapsingtoolbarexample;


class Item {
    int image;
    String title;
    public Item(int image, String title) {
        super();
        this.image = image;
        this.title = title;
    }
    public int getImage() {
        return image;
    }
    public String getTitle() {
        return title;
    }
}